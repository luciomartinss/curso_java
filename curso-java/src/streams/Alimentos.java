package streams;

public class Alimentos {

	final String nome;
	final Double peso;
	final String tipo;
	final boolean altoValorBiologico;
	
	public Alimentos(String nome, double peso, String tipo, boolean altoValorBiologico){
		this.nome = nome;
		this.peso = peso;
		this.tipo = tipo;
		this.altoValorBiologico = altoValorBiologico;
	}
}
